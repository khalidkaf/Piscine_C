#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	t;

	i = 0;
	t = argc - 1;
	while(argv[t][i])
	{
		write(1, &argv[t][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
