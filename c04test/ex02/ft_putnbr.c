#include <unistd.h>
void ft_putnbr(int nb)
{
	if(nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	if(nb < 0)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	if(nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		nb = nb + '0';
		write(1, &nb, 1);
	}
}

int main()
{
	ft_putnbr(-50);
}
